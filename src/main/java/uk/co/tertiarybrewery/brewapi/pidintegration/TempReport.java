package uk.co.tertiarybrewery.brewapi.pidintegration;

 public class TempReport {
     private Float mash;
     private Float herms;
     private Float flow;

     public Float getHlt() {
         return hlt;
     }

     public void setHlt(Float hlt) {
         this.hlt = hlt;
     }

     private Float hlt;

     public Float getMash() {
         return mash;
     }

     public void setMash(Float mash) {
         this.mash = mash;
     }

     public Float getHerms() {
         return herms;
     }

     public void setHerms(Float herms) {
         this.herms = herms;
     }

     public Float getFlow() {
         return flow;
     }

     public void setFlow(Float flow) {
         this.flow = flow;
     }
 }
